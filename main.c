int get_top_prediction(q7_t* predictions)
{
  int max_ind = 0;
  int max_val = -128;
  for(int i=0;i<10;i++) {
    if(max_val < predictions[i]) {
      max_val = predictions[i];
      max_ind = i;
    }
  }
  return max_ind;
}




void display_image(uint16_t Xpos, uint16_t Ypos, uint8_t image[32*32*3], uint16_t size, int scale)
{
	
	uint32_t color = 0;
	uint8_t r = 0;
	uint8_t g = 0;
	uint8_t b = 0;
	uint32_t lcd_width = BSP_LCD_GetXSize();
	for(int x  = 0; x<size; x++)
		for(int y =0; y<size; y++){
			int pix_loc = (y*size + x)*3;
			//calculate pixel color
			color = 0;
			r = image[pix_loc];
			g = image[pix_loc + 1];
			b = image[pix_loc + 2];
			color = 0xFF000000 | r<<16 | g<<8 | b;
			
			//BSP_LCD_DrawPixel(Xpos+x,Ypos+y, color);
			
			//these loops are for upscaling image
			
			for(int i =0; i<scale; i++){
			for (int j =0; j<scale; j++){
				BSP_LCD_DrawPixel(Xpos+scale*x+i,Ypos+scale*y+j, color);}}
			
			
		}
	
}




